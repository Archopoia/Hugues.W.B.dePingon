// Current language (default: English)
let currentLang = localStorage.getItem('preferredLanguage') || 'en';

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    // Set initial language
    applyLanguage(currentLang);

    // Update HTML lang attribute
    document.documentElement.lang = currentLang;
});

// Toggle between languages
function toggleLanguage() {
    currentLang = currentLang === 'en' ? 'fr' : 'en';
    applyLanguage(currentLang);
    localStorage.setItem('preferredLanguage', currentLang);
    document.documentElement.lang = currentLang;

    // Play a gentle chime for feedback
    if (typeof playGentleBell === 'function') {
        playGentleBell();
    }
}

// Apply language to all elements with data-i18n attribute
function applyLanguage(lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            // Store the original title attribute before translation
            const originalTitle = element.getAttribute('title');

            // Check if element has a placeholder attribute
            if (element.placeholder !== undefined) {
                element.placeholder = translations[lang][key];
            } else {
                element.textContent = translations[lang][key];
            }

            // Restore the title attribute if it existed
            if (originalTitle) {
                element.setAttribute('title', originalTitle);
            }
        }
    });

    // Update language button text and flag icon
    const langText = document.querySelector('.lang-text');
    const flagIcon = document.querySelector('#flag-icon');

    if (langText) {
        langText.textContent = lang.toUpperCase();
    }

    if (flagIcon) {
        flagIcon.src = lang === 'fr' ? 'Assets/flags/fr.svg' : 'Assets/flags/gb.svg';
        flagIcon.alt = lang === 'fr' ? 'French Flag' : 'British Flag';
    }

    // Update document title
    if (lang === 'fr') {
        document.title = "Hugues de Pingon | Lead Game Designer, Philosophe & Concepteur de Syst√®mes";
    } else {
        document.title = "Hugues de Pingon | Lead Game Designer, Philosopher & System Designer";
    }
}

